  <h1>Personvern</h1>
  <h3>
    [
    <span style="color: #3da839;">Viktig:</span>
    Dette er kun et eksempel på en personvernerklæring. Denne siden er laget for
    en eksamen og ingen data blir samlet eller brukt til noe]
  </h3>
  <h2>1 Innledning</h2>
  <p>
    VegFood er opptatt av at du skal ha tillit til oss og hvordan vi behandler
    dine personopplysninger. I denne personvernerklæringen forklarer vi derfor
    hvorfor vi samler inn informasjon om deg, hvordan vi bruker denne
    informasjonen og hvordan vi tar hensyn til ditt personvern.
  </p>
  <p>
    Personopplysninger er opplysninger og vurderinger som kan knyttes til en
    identifiserbar enkeltperson. Dette kan for eksempel være navn og
    kontaktinformasjon, kjøpshistorikk eller informasjon om interesser.
  </p>
  <p>
    Bruk av personopplysninger må følge personopplysningsloven. Vi, som
    behandlingsansvarlig, vil sørge for at vi bruker personopplysninger om deg i
    samsvar med lovens regler.
  </p>
  <h2>2 Hvilke personopplysninger har vi om deg</h2>
  <p>
    Avhengig av hvilken informasjon du gir oss, hvordan du bruker våre
    tjenester, og hvilke tillatelser du gir oss, har vi disse opplysningene om
    deg:
  </p>
  <ul>
    <li>
      <span style="font-weight: 600;">Opplysninger om deg:</span>
      Informasjon du oppgir når du oppretter en brukerkonto, som navn, adresse,
      telefonnummer og e-postadresse. Du kan velge å gi oss mer informasjon om
      deg selv, som kjønn og bursdagsdato.
    </li>
    <li>
      <span style="font-weight: 600;">Kommunikasjon:</span>
      Informasjon du gir oss når du har kontakt med vårt kundesenter, som
      eposter sendt mellom oss.
    </li>
    <li>
      <span style="font-weight: 600;">Opplysninger om bruk på nettsidene:</span>
      Informasjon vi får når du bruker vår nettside og app, som hva slags enhet
      du har (f.eks. mobil eller PC), IP-adresse, hvilke sider du besøker og
      hvilke funksjoner du bruker de
    </li>
    <li>
      <span style="font-weight: 600;">Opplysninger fra partnere:</span>
      Informasjon vi får fra våre samarbeidspartnere, som [xxx]. Partnerne gir
      oss informasjon om hvilke teaterforestillinger, konserter, kinoer og andre
      forestillinger du har besøkt.
    </li>
  </ul>
  <h2>3 Hva bruker vi personopplysningene til</h2>
  <p>Vi bruker personopplysningene dine til følgende formål:</p>
  <ul>
    <li>
      <span style="font-weight: 600;">Tilby nettside:</span>
      Vi bruker opplysninger for å tilpasse vår nettside og app til det tekniske
      utstyret du bruker. Vi kan også bruke opplysningene til å tilpasse
      innholdet på vår nettside og app til hva vi tror du er interessert i.
    </li>
    <li>
      <span style="font-weight: 600;">Opprette brukerkonto:</span>
      Vi bruker opplysninger dersom du oppretter brukerkonto, bl.a. for å gi deg
      tilgang med brukernavn og passord, for å lagre din kontaktinformasjon og
      betalingsinformasjon, og for at du kan se din kjøpshistorikk.
    </li>
    <li>
      <span style="font-weight: 600;">Kundeservice:</span>
      Vi lagrer opplysninger som du gir og mottar dersom du har kontakt med oss
      for å yte service og administrere ditt kundeforhold.
    </li>
    <li>
      <span style="font-weight: 600;">Statistikk:</span>
      Vi utarbeider statistikk og kartlegger markedstrender for å forbedre og
      videreutvikle våre produkter og tjenester. Så langt det er mulig gjør vi
      dette med anonyme opplysninger, uten at vi vet at informasjonen er knyttet
      spesifikt til deg.
    </li>
  </ul>
  <h2>4 Hvem deler vi personopplysninger med</h2>
  <p>Vi deler ingen av dataen vi har samlet</p>
  <h2>5 Hvor lenge lagrer vi personopplysninger</h2>
  <p>
    Vi lagrer opplysninger om deg, kun så lenge det er nødvendig for å oppnå
    formålet de ble innhentet for, eller dersom vi er pålagt å lagre
    opplysningene. Opplysningene vil slettes eller anonymiseres når de ikke
    lengre er nødvendige for å oppnå formålet. Under er en oversikt over hvor
    lenge vi lagrer dine opplysninger:
  </p>
  <ul>
    <li>
      <span style="font-weight: 600;">Opplysninger om deg:</span>
      Hvis du har brukerkonto hos oss, sletter vi opplysningene når du sier opp
      brukerkontoen eller hvis du har vært inaktiv i over 12 måneder. Hvis du
      ikke har brukerkonto hos oss, sletter vi opplysningene tre måneder etter
      forestillingen fant sted med mindre du ber oss om å lagre opplysninger.
    </li>
    <li>
      <span style="font-weight: 600;">Opplysninger om bruk på nettsidene:</span>
      Vi lagrer informasjon om din bruk av våre nettsider i opptil 6 måneder.
    </li>
    <li>
      <span style="font-weight: 600;">Opplysninger fra partnere:</span>
      Vi lagrer eventuell informasjon fra samarbeidspartnere så lenge du har en
      aktiv brukerkonto hos oss eller så lenge du har samtykket. Vi vil slette
      opplysningene dersom du sier opp brukerkontoen eller brukerkontoen har
      vært inaktiv i 12 måneder.
    </li>
    <li>
      <span style="font-weight: 600;">Kommunikasjon:</span>
      Vi lagrer kommunikasjon med våre kunder i tre år, med mindre vi trenger å
      lagre det lenger, f.eks. i forbindelse med klager eller krav.
    </li>
    <li>
      <span style="font-weight: 600;">Profileringsopplysninger:</span>
      Hvis du har brukerkonto hos oss, sletter vi opplysningene når du sier opp
      brukerkontoen eller brukerkontoen har vært inaktiv i over 12 måneder. Vi
      vil slette opplysningene tidligere hvis du motsetter deg å bli profilert.
    </li>
  </ul>
  <h2>6 Dine rettigheter</h2>
  <p>Du kan ha rett til å be oss å:</p>
  <ul>
    <li>
      Gi deg ytterligere informasjon om hvordan vi behandler dine
      personopplysninger.
    </li>
    <li>Gi deg en kopi av personopplysninger om deg.</li>
    <li>Oppdatere dine personopplysninger.</li>
    <li>
      Slette opplysninger som vi ikke lengre har grunnlag til å oppbevare.
    </li>
    <li>Begrense eller stoppe behandlingen av dine opplysninger</li>
    <li>
      Motsette deg automatiske individuelle avgjørelser, inkludert profilering.
    </li>
    <li>
      Gi deg opplysninger du har gitt til oss i en strukturert, alminnelig
      anvendt og maskinlesbart format eller, der det er teknisk mulig, be oss om
      å overføre disse til en annen virksomhet.
    </li>
    <li>Trekke tilbake eventuelle samtykker du har gitt oss.</li>
  </ul>
  <p>
    Vær klar over at det finnes begrensninger i disse rettighetene. Ta gjerne
    kontakt med oss hvis du vil ha mer informasjon om dette.
  </p>
  <p>
    Hvis du mener at vi behandler personopplysninger i strid med
    personopplysningsloven, har du rett til å klage til Datatilsynet. Før du
    eventuelt gjør dette, vil vi gjerne at du kontakter oss slik at vi kan
    besvare dine spørsmål eller oppklare eventuelle misforståelser.
  </p>
